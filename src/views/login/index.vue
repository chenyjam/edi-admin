<template>
  <div class="login-container">

    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">
      <div class="title-container">
        <h3 class="title">dddd</h3>

      </div>

      <el-form-item prop="userName">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          v-model="loginForm.userName"
          placeholder="账号"
          name="userName"
          type="text"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :type="passwordType"
          v-model="loginForm.password"
          placeholder="密码"
          name="password"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span
          class="show-pwd"
          @click="showPwd"
        >
          <svg-icon icon-class="eye" />
        </span>
      </el-form-item>

      <el-row :gutter="20">
        <el-col :span="18">
          <el-form-item prop="verifyCode">
            <span class="svg-container">
              <svg-icon icon-class="user" />
            </span>
            <el-input
              v-model="loginForm.verifyCode"
              placeholder="验证码"
              name="verifyCode"
              type="text"
              auto-complete="on"
            />
          </el-form-item>
        </el-col>
        <el-col :span="6">
          <img :src="verifyCode" class="login-code" @click="getVerifyCode">
        </el-col>
      </el-row>

      <el-button
        :loading="loading"
        type="primary"
        style="width:100%;margin-bottom:30px;"
        @click.native.prevent="handleLogin"
      >登录</el-button>
    </el-form>
  </div>
</template>

<script>
// 'import { isvalidusername } from '@/utils/validate'
import { login } from '@/api/login'
import { encryptByMd5 } from './md5'

export default {
  name: 'Login',
  components: { },
  data() {
    return {
      loginForm: {
        userName: '',
        password: '',
        verifyCode: ''
      },
      loginRules: {
        userName: [
          { required: true, trigger: 'blur', message: '请填写账号' }
        ],
        password: [
          { required: true, trigger: 'blur', message: '请填写密码' }
        ],
        verifyCode: [
          { required: true, trigger: 'blur', message: '请填写验证码' }
        ]
      },
      passwordType: 'password',
      loading: false,
      showDialog: false,
      redirect: undefined,
      verifyCode: '/edi-web/api/top/core?method=verifyCode'
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  created() {
    this.token()
  },
  destroyed() {
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    token() {
      this.$store
        .dispatch('GetTokenByRegisterVisitor')
        .then(() => {
        })
        .catch(() => {
          this.loading = false
        })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          const data = { ...this.loginForm, password: encryptByMd5(this.loginForm.password) }
          login(data)
            .then((rb) => {
              this.loading = false
              this.$router.push({ path: this.redirect || '/' })
            })
            .catch(() => {
              this.loading = false
            })
          // this.$store
          //   .dispatch('LoginByuserName', this.loginForm)
          //   .then(() => {
          //     this.loading = false
          //     this.$router.push({ path: this.redirect || '/' })
          //   })
          //   .catch(() => {
          //     this.loading = false
          //   })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getVerifyCode() {
      this.verifyCode = this.verifyCode + '&_=' + Math.random()
    }
  }
}
</script>

